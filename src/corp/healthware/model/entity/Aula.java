
package corp.healthware.model.entity;

public class Aula {
    private int cod_m;
    private int cod_a;
    private String data_a;
    private String hora_a;
    private String descricao;

    public int getCod_m() {
        return cod_m;
    }

    public void setCod_m(int cod_m) {
        this.cod_m = cod_m;
    }

    public int getCod_a() {
        return cod_a;
    }

    public void setCod_a(int cod_a) {
        this.cod_a = cod_a;
    }

    public String getData_a() {
        return data_a;
    }

    public void setData_a(String data_a) {
        this.data_a = data_a;
    }

    public String getHora_a() {
        return hora_a;
    }

    public void setHora_a(String hora_a) {
        this.hora_a = hora_a;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
    
    
}
